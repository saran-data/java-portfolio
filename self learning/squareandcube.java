import java.util.*;
class main{
    public static void main(String[]args){
        Scanner sc=new Scanner(System.in);
        int a = sc.nextInt();
        int square = a*a;
        int cube = a*a*a;
        System.out.println("Square of the number: "+square);
        System.out.println("Cube of the number: "+cube);
    }
}